// ColourPalette represents a palette grouping for the Game Boy Color models.
// They are essentially composed of 8 regular Palettes, 8 for BG and 8 for OBJ.
type ColourPalette [8]Palette

// A ColourisationPalette is used to apply a basic colour palette to games
// released for earlier monochrome models.
type ColourisationPalette struct {
	BG, OBJ0, OBJ1 Palette
}

