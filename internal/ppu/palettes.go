package ppu

// Palette represents one of the palettes for the original Game Boy monochrome
// models. They are composed of 4 distinct shades of gray with a very noticeable
// green tint on the earlier models. This was much less noticeable on the later
// pocket and light models.
type Palette [4][3]uint8 // 4 RGB values

func (p Palette) remap(v uint8) Palette { return Palette{p[v&3], p[v>>2&3], p[v>>4&3], p[v>>6&3]} } // remaps a palette

const (
	Greyscale = iota // default
	Green            // mimics the original green screen
	Pocket           // mimics the pockets screen
)

